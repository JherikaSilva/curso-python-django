# Peça ao usuário para digitar uma palavra. Verifique e imprima se a letra 'a' está presente na palavra.

print("----- VERIFICAR LETRA 'A' -----")
palavra= input("Digite uma palavra: ")

if "a" in palavra:
    print("A palavra contém a letra 'a'!")
else:
    print("A palavra não contém a letra 'a'")    